// Element-related utility functions
export const getElementsByCategory = (
  category: Element["category"],
): Element[] => {
  return Object.values(elements).filter(
    (element) => element.category === category,
  );
};

export const getRandomElement = (): Element => {
  const elementKeys = Object.keys(elements);
  const randomKey = elementKeys[Math.floor(Math.random() * elementKeys.length)];
  return elements[randomKey];
};

export const getElementBySymbol = (symbol: string): Element | undefined => {
  return elements[symbol];
};

export const getAllElements = (): Element[] => {
  return Object.values(elements);
};

export const getPeriodElements = (period: number): Element[] => {
  return Object.values(elements).filter((element) => element.period === period);
};

export const getGroupElements = (group: number): Element[] => {
  return Object.values(elements).filter((element) => element.group === group);
};
